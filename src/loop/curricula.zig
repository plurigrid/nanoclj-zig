fn echoStateBody(ctx: *agent_lib.Agent, _: Value) error{Invoke}!Value {
    if (ctx.state) |s| return s;
    return Value.makeInt(0);
}

fn alwaysPositiveScorer(_: Value) eval_lib.EvalError!f32 {
    return 1.0;
}


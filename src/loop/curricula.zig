fn echoStateBody(ctx: *agent_lib.Agent, _: Value) error{Invoke}!Value {
    if (ctx.state) |s| return s;
    return Value.makeInt(0);
}

fn alwaysPositiveScorer(_: Value) eval_lib.EvalError!f32 {
    return 1.0;
}

test "stateToTrit: null → 0; 1 → 1; 5 → -1 (2≡-1); 9 → 0" {
    try std.testing.expectEqual(@as(i2, 0), stateToTrit(null));
    try std.testing.expectEqual(@as(i2, 1), stateToTrit(Value.makeInt(1)));
    try std.testing.expectEqual(@as(i2, -1), stateToTrit(Value.makeInt(5)));
    try std.testing.expectEqual(@as(i2, 0), stateToTrit(Value.makeInt(9)));
}

test "lawHolds: 1+1+1 ≡ 0 (mod 3)" {
    const trits = [_]i2{ 1, 1, 1 };
    try std.testing.expect(lawHolds(&trits));
}

test "lawHolds: 0+0+0 ≡ 0" {
    const trits = [_]i2{ 0, 0, 0 };
    try std.testing.expect(lawHolds(&trits));
}

test "lawHolds: 0+1+(-1) ≡ 0 (≡ 0+1+2 in unsigned mod-3)" {
    const trits = [_]i2{ 0, 1, -1 };
    try std.testing.expect(lawHolds(&trits));
}

test "lawHolds: 1+1+0 ≢ 0" {
    const trits = [_]i2{ 1, 1, 0 };
    try std.testing.expect(!lawHolds(&trits));
}

fn setupThreeAgentExperiment(
    topo: *Topology,
    trace_store: *trace_lib.TraceStore,
    ds: *dataset_lib.Dataset,
    seeds: [3]i48,
) !void {
    const id_a = try topo.newAgent("a", echoStateBody);
    const id_b = try topo.newAgent("b", echoStateBody);
    const id_c = try topo.newAgent("c", echoStateBody);
    topo.nodes.items[id_a - 1].agent.state = Value.makeInt(seeds[0]);
    topo.nodes.items[id_b - 1].agent.state = Value.makeInt(seeds[1]);
    topo.nodes.items[id_c - 1].agent.state = Value.makeInt(seeds[2]);
    try ds.addExample(Value.makeInt(0), null, "");
    _ = trace_store;
}

test "law-merged Nash: balanced seed (1,1,1) is already a fixed point" {
    var topo = Topology.init(std.testing.allocator);
    defer topo.deinit();
    var trace_store = trace_lib.TraceStore.init(std.testing.allocator);
    defer trace_store.deinit();
    var ds = dataset_lib.Dataset.init(std.testing.allocator, "balanced");
    defer ds.deinit();
    try setupThreeAgentExperiment(&topo, &trace_store, &ds, .{ 1, 1, 1 });

    const evs = [_]eval_lib.Evaluator{eval_lib.individual("ok", alwaysPositiveScorer)};
    var exp = Experiment.init(std.testing.allocator, "balanced", &topo, &trace_store, &ds, &evs, "a");

    var traj = try cycle_lib.cycle(
        std.testing.allocator,
        &exp,
        .{ .custom = .{ .body = lawMergeStep } },
        .{ .iters = 5 },
        1,
        5,
    );
    defer traj.deinit();

    // No-op step → c remains at 1.
    try std.testing.expectEqual(@as(i48, 1), topo.nodes.items[2].agent.state.?.asInt());
    // GF(3) law still holds.
    var trits: [3]i2 = undefined;
    for (topo.nodes.items, 0..) |node, i| trits[i] = stateToTrit(node.agent.state);
    try std.testing.expect(lawHolds(&trits));
}

test "law-merged Nash: imbalanced (1,0,0) converges to a lawful triad" {
    var topo = Topology.init(std.testing.allocator);
    defer topo.deinit();
    var trace_store = trace_lib.TraceStore.init(std.testing.allocator);
    defer trace_store.deinit();
    var ds = dataset_lib.Dataset.init(std.testing.allocator, "imbalanced");
    defer ds.deinit();
    try setupThreeAgentExperiment(&topo, &trace_store, &ds, .{ 1, 0, 0 });

    const evs = [_]eval_lib.Evaluator{eval_lib.individual("ok", alwaysPositiveScorer)};
    var exp = Experiment.init(std.testing.allocator, "imbalanced", &topo, &trace_store, &ds, &evs, "a");

    var traj = try cycle_lib.cycle(
        std.testing.allocator,
        &exp,
        .{ .custom = .{ .body = lawMergeStep } },
        .{ .iters = 10 },
        1,
        10,
    );
    defer traj.deinit();

    // After enough custom-step nudges the GF(3) law holds.
    var trits: [3]i2 = undefined;
    for (topo.nodes.items, 0..) |node, i| trits[i] = stateToTrit(node.agent.state);
    try std.testing.expect(lawHolds(&trits));
    // a stayed at 1 (it's not the last agent); b stayed at 0; c was nudged.
    try std.testing.expectEqual(@as(i48, 1), topo.nodes.items[0].agent.state.?.asInt());
    try std.testing.expectEqual(@as(i48, 0), topo.nodes.items[1].agent.state.?.asInt());
    // c's trit must close the law: trits[0]=1, trits[1]=0 ⇒ trit[2]=-1
    // (equivalently, c.state mod 3 = 2).
    try std.testing.expectEqual(@as(i2, -1), stateToTrit(topo.nodes.items[2].agent.state));
}

test "law-merged Nash: imbalanced (2,2,0) converges by nudging c twice" {
    var topo = Topology.init(std.testing.allocator);
    defer topo.deinit();
    var trace_store = trace_lib.TraceStore.init(std.testing.allocator);
    defer trace_store.deinit();
    var ds = dataset_lib.Dataset.init(std.testing.allocator, "twos");
    defer ds.deinit();
    try setupThreeAgentExperiment(&topo, &trace_store, &ds, .{ 2, 2, 0 });
    // a.state=2 → trit -1; b.state=2 → trit -1; c.state=0 → trit 0.
    // Sum = -2 ≡ 1 (mod 3) ≢ 0. Need c's trit such that -1 + -1 + t ≡ 0
    // ⇒ t ≡ -1. Starting c=0, two nudges → c=2 → trit -1. ✓

    const evs = [_]eval_lib.Evaluator{eval_lib.individual("ok", alwaysPositiveScorer)};
    var exp = Experiment.init(std.testing.allocator, "twos", &topo, &trace_store, &ds, &evs, "a");

    var traj = try cycle_lib.cycle(
        std.testing.allocator,
        &exp,
        .{ .custom = .{ .body = lawMergeStep } },
        .{ .iters = 10 },
        1,
        10,
    );
    defer traj.deinit();

    var trits: [3]i2 = undefined;
    for (topo.nodes.items, 0..) |node, i| trits[i] = stateToTrit(node.agent.state);
    try std.testing.expect(lawHolds(&trits));
    try std.testing.expectEqual(@as(i2, -1), stateToTrit(topo.nodes.items[2].agent.state));
}

//! Law-merged Nash curriculum — first concrete curriculum landing on
//! the §6 SDF stack (Skill registry + Verdict union + cycle combinator).
//!
//! Categorical anchor:
//!   `goblins-adapter/propagator-nash.scm:140` — `merge-with-law`:
//!     `Merge × Law → Merge'`.
//!   Wraps a base merge so any law-violating outcome promotes to a
//!   contradiction. Here the merge IS the cycle's Step (which mutates
//!   agent state) and the law is GF(3) closure on the strategy trits.
//!
//! Setup:
//!   - N agents each carry an i48 `state` interpreted as their proposed
//!     strategy. Their body just echoes that state (the topology runs;
//!     the evaluator is satisfied by an always-positive scorer; the
//!     point of the curriculum is the inter-iteration STEP, not the
//!     evaluator output).
//!
//!   - The `lawMergeStep` body collects every agent's `(state mod 3)`
//!     trit, checks GF(3) closure (sum ≡ 0 mod 3). If the law holds,
//!     it's a no-op (the topology has reached a Nash-stable lawful
//!     point). If violated, it nudges the last agent's state +1; this
//!     is guaranteed to land on a lawful triad within ≤2 nudges for
//!     three agents (the cyclic group GF(3) is generated by +1).
//!
//! That is the SDF-permanent-extension promise paying off:
//!   - Step.custom (§6.3) gives us the structural mutation hook.
//!   - The curriculum lives in its own file with no edits to feedback.zig
//!     / cycle.zig / experiment.zig.
//!   - Adding ProTeGi-coplay or Solo-MAGICORE later follows the same
//!     pattern: another submodule, another `*Step.body` closure.

const std = @import("std");
const value = @import("../value.zig");
const Value = value.Value;

const agent_lib = @import("agent.zig");
const eval_lib = @import("eval.zig");
const dataset_lib = @import("dataset.zig");
const experiment_lib = @import("experiment.zig");
const topology_lib = @import("topology.zig");
const trace_lib = @import("trace.zig");
const cycle_lib = @import("cycle.zig");

const Topology = topology_lib.Topology;
const Experiment = experiment_lib.Experiment;
const Report = experiment_lib.Report;

/// Project an agent's i48 state to a GF(3) trit ∈ {-1, 0, +1}, matching
/// `Verdict.trit`'s `i2` convention. `state mod 3 = 2` collapses onto
/// `-1` since `2 ≡ -1 (mod 3)` in the GF(3) signed representation.
pub fn stateToTrit(state: ?Value) i2 {
    const s: i48 = if (state) |v| v.asInt() else 0;
    const m: i48 = @mod(s, 3); // 0, 1, or 2
    return switch (m) {
        0 => 0,
        1 => 1,
        2 => -1,
        else => unreachable,
    };
}

/// GF(3) closure check: sum of trits ≡ 0 (mod 3).
pub fn lawHolds(trits: []const i2) bool {
    var sum: i32 = 0;
    for (trits) |t| sum += @as(i32, t);
    return @mod(sum, 3) == 0;
}

/// The `cycle.Step.custom.body` for this curriculum.
/// Reads every agent's state, projects to GF(3) trit, checks the law;
/// if violated, nudges the last agent's state by +1 to walk toward
/// closure. (No-op when already lawful — that's the fixed point.)
pub fn lawMergeStep(experiment: *Experiment, _: *const Report) anyerror!void {
    const topo = experiment.topology;
    const n = topo.nodes.items.len;
    if (n == 0) return;

    var trits: std.ArrayListUnmanaged(i2) = .empty;
    defer trits.deinit(experiment.allocator);
    for (topo.nodes.items) |*node| {
        try trits.append(experiment.allocator, stateToTrit(node.agent.state));
    }
    if (lawHolds(trits.items)) return;

    const last = &topo.nodes.items[n - 1].agent;
    const cur: i48 = if (last.state) |v| v.asInt() else 0;
    last.state = Value.makeInt(cur + 1);
}


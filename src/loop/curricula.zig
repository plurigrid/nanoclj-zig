/// Project an agent's i48 state to a GF(3) trit ∈ {-1, 0, +1}, matching
/// `Verdict.trit`'s `i2` convention. `state mod 3 = 2` collapses onto
/// `-1` since `2 ≡ -1 (mod 3)` in the GF(3) signed representation.
pub fn stateToTrit(state: ?Value) i2 {
    const s: i48 = if (state) |v| v.asInt() else 0;
    const m: i48 = @mod(s, 3); // 0, 1, or 2
    return switch (m) {
        0 => 0,
        1 => 1,
        2 => -1,
        else => unreachable,
    };
}

/// GF(3) closure check: sum of trits ≡ 0 (mod 3).
pub fn lawHolds(trits: []const i2) bool {
    var sum: i32 = 0;
    for (trits) |t| sum += @as(i32, t);
    return @mod(sum, 3) == 0;
}

/// The `cycle.Step.custom.body` for this curriculum.
/// Reads every agent's state, projects to GF(3) trit, checks the law;
/// if violated, nudges the last agent's state by +1 to walk toward
/// closure. (No-op when already lawful — that's the fixed point.)
pub fn lawMergeStep(experiment: *Experiment, _: *const Report) anyerror!void {
    const topo = experiment.topology;
    const n = topo.nodes.items.len;
    if (n == 0) return;

    var trits: std.ArrayListUnmanaged(i2) = .empty;
    defer trits.deinit(experiment.allocator);
    for (topo.nodes.items) |*node| {
        try trits.append(experiment.allocator, stateToTrit(node.agent.state));
    }
    if (lawHolds(trits.items)) return;

    const last = &topo.nodes.items[n - 1].agent;
    const cur: i48 = if (last.state) |v| v.asInt() else 0;
    last.state = Value.makeInt(cur + 1);
}

